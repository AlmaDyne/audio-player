function createTestTracklist(n) {
    let lgN = String(n).length;

    for (let i = 1; i <= n; i++) {
        let lgI = String(i).length;

        if (lgI < lgN) {
            for (let j = 1; j <= lgN - lgI; j++) {
                i = '0' + i;
            }
        }

        tracklistsMapData.set(crypto.randomUUID(), new TestTracklistObj(i));
    }
    
    function TestTracklistObj(i) {
        this.dateUpdated = (new Date(Date.now() + Number(i))).toISOString();
        this.tracklistTitle = `Test Tracklist ${i}`;
        this.cover = 'img/def_covers/no_cover_2.png';
        this.tracks = new Array();
    
        for (let j = 1; j <= 10; j++) {
            this.tracks.push({
                id: crypto.randomUUID(),
                order: j,
                artist: 'Artist ' + j,
                title: 'Title ' + j,
                src: '#'
            });
        }
    }
}
